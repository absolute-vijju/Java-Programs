class Switch
{
	public static void main(String args[])
	{
		char name='v';
		switch(name)
		{
			case 'v':
			System.out.println("My name is Vijay Koshti");
			break;
			case 'h':
			System.out.println("My name is Hardik Koshti");
			break;
			case 'k':
			System.out.println("My name is Komal Koshti");
			break;
			default:
			System.out.println("My name is XYZ");
		}
	}
}